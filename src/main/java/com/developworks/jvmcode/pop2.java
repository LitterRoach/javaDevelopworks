public class pop2 {
    double d() {
        return 1000000000.2D;
    }
    public void pop2() {
        d();
    }
}
/**
 *   public void pop2();
 *     Code:
 *        0: aload_0
 *        1: invokevirtual #4                  // Method d:()D
 *        4: pop2
 *        5: return
 */